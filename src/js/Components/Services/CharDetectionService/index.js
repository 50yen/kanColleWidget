import {OCRServerClient} from './Implements';

export default class CharDetectionService {
  constructor(impl = new OCRServerClient('http://localhost:1999')) {
    this.impl = impl;
  }
  fromBase64(base64string) {
    const res = this.impl.fromBase64(base64string);
    if (res instanceof Promise) {
      return res;
    } else {
      return Promise.resolve(res);
    }
  }
}

